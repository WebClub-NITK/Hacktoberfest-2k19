public class AssignmentOperator {
    public static void main(String[] args) {

        int number1, number2;

        // Assigning 5 to number1
        number1 = 5;
        System.out.println(number1);

        // Assigning value of variable number2 to number1
        number2 = number1;
        System.out.println(number2);
    }
}
